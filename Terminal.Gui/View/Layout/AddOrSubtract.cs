namespace Terminal.Gui;

/// <summary>
///     Describes whether an operation should add or subtract values.
/// </summary>
public enum AddOrSubtract
{
    /// <summary>
    ///     The operation should use addition.
    /// </summary>
    Add = 0,

    /// <summary>
    ///     The operation should use subtraction.
    /// </summary>
    Subtract = 1
}

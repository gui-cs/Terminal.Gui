#nullable enable
namespace Terminal.Gui;

/// <summary>
///     Represents the state of a <see cref="CheckBox"/>.
/// </summary>
public enum CheckState
{
    /// <summary>
    ///     Neither checked nor unchecked.
    /// </summary>
    None,

    /// <summary>
    ///     Checked.
    /// </summary>
    Checked,

    /// <summary>
    ///     Not checked.
    /// </summary>
    UnChecked
}
